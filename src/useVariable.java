public class useVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String str = "Ha Noi";
        System.out.println("Số: "+i);
        System.out.println("Số thực: "+f);
        System.out.println("Số thực lớn: "+d);
        System.out.println("Logic: "+b);
        System.out.println("Ký tự: "+c);
        System.out.println("Chuỗi: "+str);
    }
}
